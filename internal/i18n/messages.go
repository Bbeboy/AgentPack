const (
	LangEN = "en"
	LangES = "es"
)

func ResolveLanguage() string {
	settings, err := config.LoadSettings()
	if err != nil {
		return LangEN
	}
	if settings.Language == LangES {
		return LangES
	}
	return LangEN
}

func Message(lang, key string, args ...any) string {
	tmpl, ok := messageCatalog[lang][key]
	if !ok {
		tmpl = messageCatalog[LangEN][key]
	}
	if tmpl == "" {
		return key
	}
	return fmt.Sprintf(tmpl, args...)
}

